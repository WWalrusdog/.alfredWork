#!/usr/bin/python
def longreplace(codon):
    codon = codon.replace("AAA","Lys")
    codon = codon.replace("AAC","Asn")
    codon = codon.replace("AAG","Lys")
    codon = codon.replace("AAU","Asn")
    codon = codon.replace("ACA","Thr")
    codon = codon.replace("ACC","Thr")
    codon = codon.replace("ACG","Thr")
    codon = codon.replace("ACU","Thr")
    codon = codon.replace("AGA","Arg")
    codon = codon.replace("AGC","Ser")
    codon = codon.replace("AGG","Arg")
    codon = codon.replace("AGU","Ser")
    codon = codon.replace("AUA","Ile")
    codon = codon.replace("AUC","Ile")
    codon = codon.replace("AUG","Met")
    codon = codon.replace("AUU","Val")
    codon = codon.replace("CAA","Gln")
    codon = codon.replace("CAC","His")
    codon = codon.replace("CAG","Gln")
    codon = codon.replace("CAU","His")
    codon = codon.replace("CCA","Pro")
    codon = codon.replace("CCC","Pro")
    codon = codon.replace("CCG","Pro")
    codon = codon.replace("CCU","Pro")
    codon = codon.replace("CGA","Arg")
    codon = codon.replace("CGC","Arg")
    codon = codon.replace("CGG","Arg")
    codon = codon.replace("CGU","Arg")
    codon = codon.replace("CUA","Leu")
    codon = codon.replace("CUC","Leu")
    codon = codon.replace("CUG","Leu")
    codon = codon.replace("CUU","Leu")
    codon = codon.replace("GAA","Glu")
    codon = codon.replace("GAC","Asp")
    codon = codon.replace("GAG","Glu")
    codon = codon.replace("GAU","Asp")
    codon = codon.replace("GCA","Ala")
    codon = codon.replace("GCC","Ala")
    codon = codon.replace("GCG","Ala")
    codon = codon.replace("GCU","Ala")
    codon = codon.replace("GGA","Gly")
    codon = codon.replace("GGC","Gly")
    codon = codon.replace("GGG","Gly")
    codon = codon.replace("GGU","Gly")
    codon = codon.replace("GUA","Val")
    codon = codon.replace("GUC","Val")
    codon = codon.replace("GUG","Val")
    codon = codon.replace("UAA","STOP")
    codon = codon.replace("UAC","Tyr")
    codon = codon.replace("UAG","STOP")
    codon = codon.replace("UAU","Tyr")
    codon = codon.replace("UCA","Ser")
    codon = codon.replace("UCC","Ser")
    codon = codon.replace("UCG","Ser")
    codon = codon.replace("UCU","Ser")
    codon = codon.replace("UGA","STOP")
    codon = codon.replace("UGC","Cys")
    codon = codon.replace("UGG","Trp")
    codon = codon.replace("UGU","Cys")
    codon = codon.replace("UUA","Leu")
    codon = codon.replace("UUC","Phe")
    codon = codon.replace("UUG","Leu")
    codon = codon.replace("UUU","Phe")
    return codon
translated = ""
nucleotides_ = open("nucleotides.txt","r")
nucleotides = nucleotides_.read()
nucleotides_.close()
aa_length = int(len(nucleotides)/3)
for i in range(aa_length):
    codon = nucleotides[:3]
    codon = longreplace(codon)
    translated = translated + codon + "-"
    nucleotides = nucleotides[3:]
translated = translated.rstrip("-")
translated_ = open("translated.txt", "w")
translated_.write(translated)
translated_.close()